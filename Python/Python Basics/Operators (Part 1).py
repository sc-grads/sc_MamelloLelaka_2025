
a, b = 2, 4
print("a =", a, "b =", b)
print("addition        →", a + b)
print("subtraction     →", a - b)
print("multiplication  →", a * b)
print("division        →", a / b)


print("4 // 3  →", 4 // 3)
print("5 // 3  →", 5 // 3)
print("15 // 4 →", 15 // 4)


print("2 ** 4 →", 2 ** 4)


print("3 % 2  →", 3 % 2)
print("10 % 7 →", 10 % 7)


print("10 + 10 * 2      =", 10 + 10 * 2)
print("(10 + 10) * 2    =", (10 + 10) * 2)


x = 2
print("Start x =", x)

x += 2        # same as:  x = x + 2
print("after x += 2 →", x)

x -= 2        # x = x - 2
print("after x -= 2 →", x)